package cz.zcu.kiv.oop.uml.element;

/**
 * Determines direction type of UML relation.
 *
 * @author Mr.FrAnTA (Michal Dékány)
 */
public enum UmlRelationOrientationType {

  /** Relation orientation from left to right. */
  RIGHT,
  /** Relation orientation from right to left. */
  LEFT,
  /** Relation orientation for both directions. */
  UNI,
  /** Undefined relation orientation type. */
  UNDEFINED,
  ;

}
